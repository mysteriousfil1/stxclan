function updateLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });
    
    // Set direction and lang attribute
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    
    localStorage.setItem('stx_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    // Initial Language Load
    const savedLang = localStorage.getItem('stx_lang') || 'ar';
    const langSelect = document.getElementById('lang-select');
    if (langSelect) {
        langSelect.value = savedLang;
        langSelect.addEventListener('change', (e) => updateLanguage(e.target.value));
    }
    updateLanguage(savedLang);

    // 1. Loading Screen Animation
    window.addEventListener('load', () => {
        const loading = document.getElementById('loading-screen');
        const progress = document.querySelector('.progress');
        
        let width = 0;
        const interval = setInterval(() => {
            if (width >= 100) {
                clearInterval(interval);
                gsap.to(loading, { opacity: 0, duration: 0.5, onComplete: () => {
                    loading.style.display = 'none';
                    initAnimations();
                }});
            } else {
                width += Math.random() * 15;
                progress.style.width = Math.min(width, 100) + '%';
            }
        }, 100);
    });

    // 2. Fire and Smoke Background Effect
    initParticles();
    
    // Mouse Glow Setup
    const glow = document.createElement('div');
    glow.className = 'mouse-glow';
    document.body.appendChild(glow);

    document.addEventListener('mousemove', (e) => {
        gsap.to(glow, { 
            left: e.clientX, 
            top: e.clientY, 
            duration: 0.1, 
            ease: "none" 
        });
    });

    // 3. GSAP Animations for Content
    function initAnimations() {
        gsap.to('.reveal-text', { opacity: 1, y: 0, duration: 1, stagger: 0.2, ease: "power4.out" });
        gsap.to('.reveal-text-big', { opacity: 1, scale: 1, duration: 1.2, ease: "back.out(1.7)" });
        
        // Scroll Magic style Reveal
        const cards = document.querySelectorAll('.reveal-card, .status-section, .leaderboard-section');
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if(entry.isIntersecting) {
                    gsap.to(entry.target, { opacity: 1, y: 0, duration: 0.8, ease: "power2.out" });
                }
            });
        }, { threshold: 0.1 });

        cards.forEach(card => {
            card.style.opacity = '0';
            card.style.transform = 'translateY(40px)';
            observer.observe(card);
        });
    }

    // 4. Server Status Live Fetching
    const fetchServerStatus = async () => {
        try {
            const response = await fetch('/api/server-status');
            const data = await response.json();
            
            document.getElementById('server-name').innerText = data.name;
            document.getElementById('server-players').innerText = `${data.players}/${data.maxplayers}`;
            document.getElementById('server-map').innerText = data.map;
            
            const percentage = (data.players / data.maxplayers) * 100;
            document.getElementById('player-progress').style.width = `${percentage}%`;
            
            const indicator = document.querySelector('.status-indicator');
            if (data.status === 'Online') {
                indicator.classList.remove('offline');
                indicator.classList.add('online');
            } else {
                indicator.classList.remove('online');
                indicator.classList.add('offline');
            }
        } catch (err) {
            console.error('Failed to fetch server status:', err);
        }
    };

    if (document.getElementById('server-status')) {
        fetchServerStatus();
        setInterval(fetchServerStatus, 30000); // 30s update
    }

    // 5. Leaderboard Live Fetching
    const fetchLeaderboard = async () => {
        const lbBody = document.getElementById('leaderboard-body');
        if(!lbBody) return;

        try {
            const response = await fetch('/api/leaderboard');
            const data = await response.json();
            
            lbBody.innerHTML = '';
            data.forEach((player, index) => {
                const rankClass = index < 3 ? `rank-${index + 1}` : '';
                const row = `
                    <tr class="${rankClass}">
                        <td><span class="rank-text">${index + 1}</span></td>
                        <td>
                            <div class="player-info">
                                <img src="${player.avatar || 'https://cdn.discordapp.com/embed/avatars/0.png'}" class="mini-avatar">
                                <span class="player-name">${player.playerName}</span>
                            </div>
                        </td>
                        <td>${player.kills}</td>
                        <td>${player.deaths}</td>
                        <td>${player.score}</td>
                    </tr>
                `;
                lbBody.insertAdjacentHTML('beforeend', row);
            });
        } catch (err) {
            console.error('Failed to fetch leaderboard:', err);
        }
    };

    if (document.getElementById('leaderboard')) {
        fetchLeaderboard();
    }

    // 6. User Profile Fetching (Dashboard and Navbar)
    const fetchUserData = async () => {
        try {
            const response = await fetch('/api/user');
            if (response.ok) {
                const user = await response.json();
                
                // Update Login button in Navbar
                const authBtn = document.querySelector('.auth-btn');
                if(authBtn) {
                    const avatarUrl = user.avatar 
                        ? `https://cdn.discordapp.com/avatars/${user.discord_id}/${user.avatar}.png`
                        : `https://cdn.discordapp.com/embed/avatars/0.png`;
                    authBtn.innerHTML = `
                        <div class="user-pill">
                            <img src="${avatarUrl}" alt="Avatar" class="mini-avatar">
                            <span>${user.username}</span>
                        </div>
                    `;
                }

                // Update Dashboard elements if on Dashboard page
                if (document.body.classList.contains('dashboard-page')) {
                    const avatarEl = document.getElementById('user-avatar');
                    const avatarUrl = user.avatar 
                        ? `https://cdn.discordapp.com/avatars/${user.discord_id}/${user.avatar}.png`
                        : `https://cdn.discordapp.com/embed/avatars/0.png`;
                    if(avatarEl) avatarEl.src = avatarUrl;
                    
                    const nameEl = document.getElementById('user-name');
                    if(nameEl) nameEl.innerText = user.username;
                    
                    const idEl = document.getElementById('user-id');
                    if(idEl) idEl.innerText = `ID: ${user.discord_id}`;

                    const subDaysEl = document.getElementById('user-sub-days');
                    const pkgEl = document.getElementById('user-pkg-name');
                    const playtimeEl = document.getElementById('user-playtime');
                    const kdEl = document.getElementById('user-kd');
                    
                    if(subDaysEl) {
                        if (user.subscriptionExpiry) {
                            const remaining = Math.ceil((new Date(user.subscriptionExpiry) - new Date()) / (1000 * 60 * 60 * 24));
                            subDaysEl.innerText = Math.max(0, remaining);
                        } else {
                            subDaysEl.innerText = '0';
                        }
                    }
                    if(pkgEl) {
                        pkgEl.innerText = user.subscriptionPackage || 'None';
                    }
                    if(playtimeEl) {
                        playtimeEl.innerText = user.playtime || '0';
                    }
                    if(kdEl) {
                        kdEl.innerText = user.kd || '0.0';
                    }

                    // Add Admin Link if the user is an admin in DB
                    if (user.isAdmin) {
                        const menu = document.querySelector('.dashboard-menu');
                        if (menu && !document.getElementById('admin-link')) {
                            const adminLink = document.createElement('a');
                            adminLink.href = 'admin.html';
                            adminLink.id = 'admin-link';
                            adminLink.innerHTML = '<i class="fas fa-user-shield"></i> <span>الإدارة</span>';
                            menu.prepend(adminLink);
                        }
                    }
                }
            }
        } catch (err) {
            // Probably not logged in, ignore
        }
    };

    fetchUserData();

    // 7. Particle System (Fire/Smoke)
    function initParticles() {
        const fireContainer = document.getElementById('fire-container');
        const smokeContainer = document.getElementById('smoke-container');
        if(!fireContainer || !smokeContainer) return;

        function createParticle(container, type) {
            const particle = document.createElement('div');
            particle.classList.add(type === 'fire' ? 'fire-particle' : 'smoke-particle');
            
            const size = Math.random() * (type === 'fire' ? 20 : 100) + 5;
            particle.style.width = size + 'px';
            particle.style.height = size + 'px';
            particle.style.left = Math.random() * 100 + '%';
            particle.style.bottom = '-50px';
            
            container.appendChild(particle);
            
            gsap.to(particle, {
                y: -(Math.random() * 500 + 400),
                x: (Math.random() - 0.5) * 200,
                opacity: 0,
                scale: type === 'fire' ? 0.2 : 2,
                duration: Math.random() * 3 + 2,
                ease: 'power1.out',
                onComplete: () => {
                    particle.remove();
                    createParticle(container, type);
                }
            });
        }

        // Initial burst
        for (let i = 0; i < 20; i++) setTimeout(() => createParticle(fireContainer, 'fire'), i * 150);
        for (let i = 0; i < 15; i++) setTimeout(() => createParticle(smokeContainer, 'smoke'), i * 200);
    }
    // 8. Chat Widget Logic
    const chatBubble = document.getElementById('chat-bubble');
    const chatWindow = document.getElementById('chat-window');
    const closeChat = document.getElementById('close-chat');
    const chatBody = document.getElementById('chat-body');
    const replyBtns = document.querySelectorAll('.reply-btn');

    if (chatBubble) {
        chatBubble.addEventListener('click', () => {
            chatWindow.classList.toggle('active');
        });

        if (closeChat) {
            closeChat.addEventListener('click', (e) => {
                e.stopPropagation();
                chatWindow.classList.remove('active');
            });
        }

        const responses = {
            vip_info: "نوفر 3 مستويات من الـ VIP: الماسي، الملك الذهبي، والفضي. كل باقة تمنحك تخطي طابور الانتظار (Whitelist) ومميزات حصرية في الديسكورد واللعبة.",
            how_to_sub: "يمكنك الاشتراك عبر متجر VIP في الموقع أو من خلال الديسكورد مباشرة. اختر الباقة التي تناسبك واتبع التعليمات.",
            discord_link: "يمكنك الانضمام لمجتمعنا عبر الرابط: https://discord.gg/stx1 - تواصل مع الإدارة هناك لأي استفسار."
        };

        replyBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                const type = btn.getAttribute('data-reply');
                if (!type) return;

                // Use current language for responses
                const currentLang = localStorage.getItem('selectedLang') || 'ar';
                const ansKey = `chat_${type}_ans`;
                const botText = translations[currentLang][ansKey] || "We are here to help!";

                const userText = btn.innerText;

                // Add user message
                const userMsg = document.createElement('div');
                userMsg.className = 'chat-msg user-msg';
                userMsg.innerText = userText;
                chatBody.appendChild(userMsg);

                // Add bot response
                setTimeout(() => {
                    const botMsg = document.createElement('div');
                    botMsg.className = 'chat-msg bot-msg';
                    botMsg.innerText = botText;
                    chatBody.appendChild(botMsg);
                    chatBody.scrollTop = chatBody.scrollHeight;
                }, 600);
                
                chatBody.scrollTop = chatBody.scrollHeight;
            });
        });
    }
});
